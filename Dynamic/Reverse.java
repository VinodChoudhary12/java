import java.util.Scanner;
class Reverse
{
   public static void main(String args[])
   {
      Scanner sc = new Scanner(System.in);
      System.out.println("Enter Five Numbers To Get Reverse Order -->");
      System.out.println("\n\n First Number  :");
      int a=sc.nextInt();
      System.out.println(" Second Number :");
      int b=sc.nextInt();
      System.out.println(" Third Number  :");
      int c=sc.nextInt();
      System.out.println(" Fourth Number :");
      int d=sc.nextInt();
      System.out.println(" Fifth Number  :");
      int e=sc.nextInt();
      System.out.println(" Reverse Order is -->"+e+","+d+","+c+","+b+","+a);
      
   }
}
